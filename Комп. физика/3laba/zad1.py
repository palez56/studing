import matplotlib.pyplot as plt
import numpy as np
q=float(input("Q="))
l=float(input("l="))
e0=8.95*10**(-12)
x = []
y = []

for r in range(1,20):
    e = (1/(4*np.pi*e0))*2*q*l/(r**3)
    x.append(r)
    y.append(e)
    
print("E = ", e)
plt.plot(x,y, linewidth=4, color='plum')
plt.show()